SELECT
Q.ID,
Q.YEAR,
ISNULL(Y.NPV, 0) AS NPV
FROM
Queries AS Q
LEFT OUTER JOIN
Year_tbl AS Y
ON
Q.ID = Y.ID AND Q.YEAR = Y.YEAR;
